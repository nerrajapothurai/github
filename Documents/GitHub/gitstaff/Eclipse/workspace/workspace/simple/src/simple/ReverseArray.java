package simple;

public class ReverseArray {
	public static void main(String[] args) {
		int[] X=new int[6];
		      X[0]=1;
				X[1]=2;
				X[2]=3;
				X[3]=4;
				X[4]=5;
				X[5]=6;
		System.out.println(X.length);
		System.out.println("reverse array is:");
		for(int i=0; i>X.length;i--)
		{
			System.out.println(X[i]+"");
		}
		
		
	}

}
